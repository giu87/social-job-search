package it.expertfinding.datamodel.resources.linkedin;

import it.expertfinding.utils.linkedin.Linkedin;

public class LinkedinAttachment {

   @Linkedin
   private String summary;
   @Linkedin
   private String title;
   @Linkedin
   private String contentDomain;
   @Linkedin
   private String contentUrl;
   @Linkedin
   private String imageUrl;

   public String getSummary() {
      return this.summary;
   }

   public void setSummary(String summary) {
      this.summary = summary;
   }

   public String getTitle() {
      return this.title;
   }

   public void setTitle(String title) {
      this.title = title;
   }

   public String getContentDomain() {
      return this.contentDomain;
   }

   public void setContentDomain(String contentDomain) {
      this.contentDomain = contentDomain;
   }

   public String getContentUrl() {
      return this.contentUrl;
   }

   public void setContentUrl(String contentUrl) {
      this.contentUrl = contentUrl;
   }

   public String getImageUrl() {
      return this.imageUrl;
   }

   public void setImageUrl(String imageUrl) {
      this.imageUrl = imageUrl;
   }
}
