package it.expertfinding.datamodel.resources.facebook;

import com.restfb.Facebook;

public class FacebookLocation {

   @Facebook
   private Double latitude;

   @Facebook
   private Double longitude;

   @Facebook
   private String city;

   @Facebook
   private String state;

   @Facebook
   private String country;

   @Facebook
   private String zip;

   /**
    * The latitude of the check-in.
    * 
    * @return The latitude of the check-in.
    */
   public Double getLatitude() {
      return latitude;
   }

   public void setLatitude(Double latitude) {
      this.latitude = latitude;
   }

   /**
    * The longitude of the check-in.
    * 
    * @return The longitude of the check-in.
    */
   public Double getLongitude() {
      return longitude;
   }

   public void setLongitude(Double longitude) {
      this.longitude = longitude;
   }

   /**
    * The city of the check-in.
    * 
    * @return The city of the check-in.
    * @since 1.6.5
    */
   public String getCity() {
      return city;
   }

   public void setCity(String city) {
      this.city = city;
   }

   /**
    * The state of the check-in.
    * 
    * @return The state of the check-in.
    * @since 1.6.5
    */
   public String getState() {
      return state;
   }

   public void setState(String state) {
      this.state = state;
   }

   /**
    * The country of the check-in.
    * 
    * @return The country of the check-in.
    * @since 1.6.5
    */
   public String getCountry() {
      return country;
   }

   public void setCountry(String country) {
      this.country = country;
   }

   /**
    * The postal code of this location.
    * 
    * @return The postal code of this location.
    */
   public String getZip() {
      return zip;
   }

   public void setZip(String zip) {
      this.zip = zip;
   }

}
