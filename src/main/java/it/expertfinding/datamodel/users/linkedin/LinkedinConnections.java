package it.expertfinding.datamodel.users.linkedin;

import it.expertfinding.datamodel.resources.linkedin.LinkedinUsersList;
import it.expertfinding.utils.linkedin.Linkedin;

public class LinkedinConnections extends LinkedinUsersList {

   @Linkedin("_count")
   private Long count;
   @Linkedin("_start")
   private Long start;

   public Long getCount() {
      return this.count;
   }

   public void setCount(Long count) {
      this.count = count;
   }

   public Long getStart() {
      return this.start;
   }

   public void setStart(Long start) {
      this.start = start;
   }
}
