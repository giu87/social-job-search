package it.expertfinding.datamodel.users.linkedin;

import it.expertfinding.utils.linkedin.Linkedin;

import java.util.List;

public class LinkedinEducations {

   @Linkedin("values")
   protected List<LinkedinEducation> educationsList;
   @Linkedin("_total")
   protected Long total;

   public List<LinkedinEducation> getEducationsList() {
      return this.educationsList;
   }

   public void setEducationsList(List<LinkedinEducation> educationsList) {
      this.educationsList = educationsList;
   }

   public Long getTotal() {
      return this.total;
   }

   public void setTotal(Long total) {
      this.total = total;
   }
}
